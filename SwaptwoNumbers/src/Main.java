
public class Main {
	public static void main(String[] args) {
		//Declaring the variables and assignment
		int x = 20;
		int y = 90;
		
		System.out.println("The values of x and y before Swapping");
		System.out.println(x);
		System.out.println(y);
		
		//Swapping the variableces
		int temp = x;
		x=y;
		y=temp;
		
		System.out.println("The values of x and y after Swapping");
		System.out.println(x);
		System.out.println(y);
		
		
		
	}

}
